class words extends GetxController {
  double angle=0;
  Set<String> strPermutations = {};
  Set<String> eng = {};
  void rot(){
    Timer.periodic(Duration(milliseconds: 50), (timer) {angle+=0.1;update();
    });
  }
  void reset() {
    strPermutations.clear();
    update();
  }

  @override
  void onInit() {
    super.onInit();
    english();rot();
  }

  Future<void> english() async {
    strPermutations.forEach((element) async {
      for (int i = 0; i < englishwords.all.length; i++) {
        if (englishwords.all[i].compareTo(element) == 0) eng.add(element);
        update();
      }
    });
  }

  Future<void> findwords(String source) async {
    for (int i = source.length - 1; i >= 0; i--) {
      for (int j = source.length - 1; j >= 0; j--) {
        String rep = source;
        if (i >= j) continue;
        findAllPermutations(rep.replaceRange(i, j, ""));
        findAllPermutations(source);
      }
    }
  }

  Future<void> findwords1(String source) async {
    for (int x = 0; x < source.length; x++) {
      for (int y = 0; y < source.length; y++) {
        String rep = source;
        if (x > y) continue;
        // findAllPermutations(rep.replaceRange(x, y, ""));
        findAllPermutations(rep.split("").getRange(x, y).join());
      }
    }
  }

  Future<void> findall1(String source) async {
    findAllPermutations(source);
    var rep = source.split("");
    for (int i = source.length - 1; i > 0; i--) {
      rep = source.split("");
      String r = rep.removeAt(i);
      findAllPermutations(rep.join());
    }
    if (rep.isEmpty) return;
    findall(rep.join());
  }

  Future<void> findall9(String source) async {
    findAllPermutations(source);
    var rep = source.split("");
    for (int i = source.length - 1; i > 0; i--) {
      rep = source.split("");
      String r = rep.removeAt(i);
      findAllPermutations(rep.join());
    }
    if (rep.isEmpty) return;
    findall(rep.reversed.join());
  }

  Future<void> findall2(String source) async {
    findAllPermutations(source);
    var rep = source.split("");
    for (int i = source.length - 1; i >= 0; i--) {
      rep = source.split("");
