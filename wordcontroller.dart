      findAllPermutations(rep.join());
    }
    if (rep.isEmpty) return;
    findall(rep.join());
  }

  Future<void> findall8(String source) async {
    findAllPermutations(source);
    var rep = source.split("");
    for (int i = source.length - 1; i >= 0; i--) {
      rep = source.split("");
      String r = rep.removeAt(i);
      findAllPermutations(rep.reversed.join());
    }
    if (rep.isEmpty) return;
    findall(rep.reversed.join());
  }

  Future<void> findall(String source) async {
    findAllPermutations(source);
    var rep = source.split("");
    for (int i = 0; i < source.length; i++) {
      rep = source.split("");
      String r = rep.removeAt(i);
      findAllPermutations(rep.join());
    }
    if (rep.isEmpty) return;
    findall(rep.join());
  }

  Future<void> findall3(String source) async {
    findAllPermutations(source);
    var rep = source.split("");
    for (int i = 0; i < source.length - 1; i++) {
      rep = source.split("");
      String r = rep.removeAt(i);
      findAllPermutations(rep.join());
    }
    if (rep.isEmpty) return;
    findall(rep.join());
  }

  Future<void> findall7(String source) async {
    findAllPermutations(source);
    var rep = source.split("");
    for (int i = 0; i < source.length - 1; i++) {
      rep = source.split("");
      String r = rep.removeAt(i);
      findAllPermutations(rep.reversed.join());
    }
    if (rep.isEmpty) return;
    findall(rep.reversed.join());
  }

  Future<void> findall4(String source) async {
    findAllPermutations(source);
    var rep = source.split("");
    for (int i = 0; i < source.length; i++) {
      rep = source.split("");
      String r = rep.removeAt(i);
      findAllPermutations(rep.reversed.join());
    }
    findAllPermutations(rep.reversed.join());
    if (rep.isEmpty) return;
    findall(rep.join());
  }

  Future<void> findall5(String source) async {
    findAllPermutations(source);
    var rep = source.split("");
    for (int i = 0; i < source.length - 3; i++) {
      rep = source.split("");
      String r = rep.removeAt(i);
      findAllPermutations(rep.reversed.join());
    }
    if (rep.isEmpty) return;
    findall(rep.reversed.join());
  }

  Future<void> findall6(String source) async {
    findAllPermutations(source);
    var rep = source.split("");
    for (int i = 0; i < source.length - 3; i++) {
      rep = source.split("");
      String r = rep.removeAt(i);
      findAllPermutations(rep.join());
    }
    if (rep.isEmpty) return;
    findall(rep.join());
  }

  Future<void> findAllPermutations(String source) async {
    List allPermutations = [];
    void permutate(List list, int cursor) {
      // when the cursor gets this far, we've found one permutation, so save it
      if (cursor == list.length) {
        allPermutations.add(list);
      }

      for (int i = cursor; i < list.length; i++) {
        List permutation = new List.from(list);
        permutation[cursor] = list[i];
        permutation[i] = list[cursor];
        permutate(permutation, cursor + 1);
      }
    }

    permutate(source.split(''), 0);

    for (List permutation in allPermutations) {
      //await Future.delayed(Duration(milliseconds: 500));
      strPermutations.add(permutation.join());
      update();
    }
  }
}

